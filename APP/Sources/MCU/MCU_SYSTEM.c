/*--------------------------------------------------------------------------*/
/* Function  | MCU Reset by SW                                              */
/* Name      | MCU_SYSTEM_SWReset                                           */
/*--------------------------------------------------------------------------*/
/* Parameter | Nothing                                                      */
/* Return    | Nothing                                                      */
/*--------------------------------------------------------------------------*/
void MCU_SYSTEM_SWReset(void)
{
	// CPMURFLG_COPRF : After SW reset, plz check this Register
	CPMUCOP = 0x01u;
	CPMUARMCOP = 0x66u;
	CPMUARMCOP = 0xAAu;
	while(1){};
}


/*--------------------------------------------------------------------------*/
/* Function  | Check the BootLoader is exist                                */
/* Name      | Check_Exist_BootLoader                                       */
/*--------------------------------------------------------------------------*/
/* Parameter | Nothing                                                      */
/* Return    | Nothing                                                      */
/*--------------------------------------------------------------------------*/
bool Check_Exist_BootLoader(void)
{
	if ((*(unsigned int *) BootLoader_version_Address) != 0xFFFF)
		return TRUE;
	else
		return FALSE;
	
	return FALSE;
}
